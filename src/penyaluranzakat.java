    private void namacomboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_namacomboActionPerformed
        // TODO add your handling code here:
        String nama = (String) namacombo.getSelectedItem();
        try {
            String sql = "SELECT * FROM tbmustahik WHERE nama_mustahik='" + 
                    nama+"'";
            Connection con = koneksi.getKoneksi();
            Statement sta = (Statement) con.createStatement();
            ResultSet res = sta.executeQuery(sql);
            while (res.next()) {
            alamat.setText(res.getString("alamat"));
            notelp.setText(res.getString("notelp"));
          
            
            
        }
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Gagal");
        }
        
    }//GEN-LAST:event_namacomboActionPerformed

