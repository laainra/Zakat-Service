    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String id, tgl,nama, al,no, bz, idc,slr;
        id = idsalur.getText();
        tgl = tanggal.getText();
        nama = (String) namacombo.getSelectedItem();
        al = alamat.getText();
        no = notelp.getText();
        idc =  (String) idcom.getSelectedItem();
        bz = besarzakat.getText();
        slr = "Telah Disalurkan";
        DefaultTableModel tbl = (DefaultTableModel) tabsalur.getModel();
        Connection con = koneksi.getKoneksi();
        
        
        try {   
             
            
            Statement sta = (Statement) con.createStatement();
            String sql = "SELECT * FROM tbsalur WHERE id_salur='" + id
                    + "'";
            ResultSet res = sta.executeQuery(sql);
            
            if(res.next()){
                
                JOptionPane.showMessageDialog(null,"ID Sudah Terdaftar", "Kesalahan",
                            JOptionPane.INFORMATION_MESSAGE);
                clearForm();
            res.close();
            }
            else { 
                try {
            
            String query = "INSERT INTO `tbsalur`(`id_salur`, `tanggal_salur`, `nama_mustahik`, `alamat_mustahik`, `no_telp`, `besar_zakat`) VALUES ('" 
                    +id+
                    "','"+tgl+
                    "','"+nama+
                    "','"+al+
                    "','"+no+
                    "','"+bz+
                    "')";
            
            PreparedStatement ps = (PreparedStatement)con.prepareStatement(query);
            ps.execute();
            JOptionPane.showMessageDialog(null, "Berhasil Menyimpan");
            System.out.print(idc);
            try {
            
            String que ="UPDATE `datazakat` SET `status_penyaluran`= '"+slr +"' WHERE id_zakat='"+idc+"'";
            PreparedStatement p = (PreparedStatement)con.prepareStatement(que);
            p.executeUpdate();
            
            
            } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Gagal");
            }
            res.close();
             
            
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Gagal Menyimpan", "Kesalahan",
                            JOptionPane.INFORMATION_MESSAGE);
        }
                 
        
           
        }
        
            } 
        
        
         catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error");
        }
         
       
        
       
           clearForm();
           showData();
        
        
        

            
    }//GEN-LAST:event_simpanActionPerformed

