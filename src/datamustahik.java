    private void tabmusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabmusMouseClicked
        // TODO add your handling code here:
        int row =  tabmus.rowAtPoint(evt.getPoint());
        String id_mus = tabmus.getValueAt(row, 0).toString();
        idmustahik.setText(id_mus);
        
        String nama = tabmus.getValueAt(row, 1).toString();
        namamustahik.setText(nama);
        
        if ("Perempuan".equals(tabmus.getValueAt(row, 2).toString())){
                perempuan.setSelected(true);
        }else {
                laki.setSelected(true);
        }
        
        String  jm = tabmus.getValueAt(row, 3).toString();
        jenismustahik.setSelectedItem(jm);
        
        String al = tabmus.getValueAt(row, 4).toString();
        alamat.setText(al);
        
        String no = tabmus.getValueAt(row, 5).toString();
        notelp.setText(no);
        
    }//GEN-LAST:event_tabmusMouseClicked

