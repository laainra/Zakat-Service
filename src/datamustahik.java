    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String idmus = idmustahik.getText();
        String namamus = namamustahik.getText();
        String jk = null;
        String jmus = (String) jenismustahik.getSelectedItem();
        String address = alamat.getText();
        String numb = notelp.getText();
        DefaultTableModel tbl = (DefaultTableModel) tabmus.getModel();
        Connection con = koneksi.getKoneksi();

        
        if (laki.isSelected()) {
            jk = "Laki-Laki";
        
        } else if (perempuan.isSelected()){
            jk = "Perempuan";
        }
        
        try {                                       
            Statement sta = (Statement) con.createStatement();
            String sql = "SELECT * FROM tbmustahik WHERE id_mustahik='" + idmus 
                    + "'";
            
            ResultSet res = sta.executeQuery(sql);
            
            if(res.next()){
                
                JOptionPane.showMessageDialog(null,"ID Sudah Terdaftar", "Kesalahan",
                            JOptionPane.INFORMATION_MESSAGE);
                    clearForm();
            
            }
            else {
                try {
            
            String query = "INSERT INTO `tbmustahik`(`id_mustahik`,"
                    + " `nama_mustahik`, `jenis_kelamin`, `jenis_mustahik`, `alamat`, `notelp`) "
                    + "VALUES ('" +idmus+
                    "','"+namamus +
                    "','"+jk+
                    "','"+jmus+
                    "','"+address+
                    "','"+numb+"')";
            
            PreparedStatement ps = (PreparedStatement)con.prepareStatement(query);
            ps.execute();
            JOptionPane.showMessageDialog(null, "Berhasil Menyimpan");
            clearForm();
            showData();
            
        } catch (SQLException ex) {
            
        }
            
            
            }
        
        } 
        
        catch (SQLException ex) {
           
        }
                
            

    }//GEN-LAST:event_simpanActionPerformed

