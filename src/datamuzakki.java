    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String idmuz = idmuzakki.getText();
        String namamuz = NamaMuzakki.getText();
        String jk = null;
        String address = alamat.getText();
        String numb = notelp.getText();
        DefaultTableModel tbl = (DefaultTableModel) tabmuzakki.getModel();
        Connection con = koneksi.getKoneksi();
       
     
        if (lakilaki.isSelected()) {
            jk = "Laki-Laki";
        
        } else if (perempuan.isSelected()){
            jk = "Perempuan";
        }
        
        
        
        try {                                       
            Statement sta = (Statement) con.createStatement();
            String sql = "SELECT * FROM tbmuzakki WHERE id_muzakki='" + idmuz 
                    + "'";
            
            ResultSet res = sta.executeQuery(sql);
            
            if(res.next()){
                
                JOptionPane.showMessageDialog(null,"ID Sudah Terdaftar", "Kesalahan",
                            JOptionPane.INFORMATION_MESSAGE);
                    clearForm();
            
            }
            else {
                try {
            
            String query = "INSERT INTO `tbmuzakki`(`id_muzakki`, `nama_muzakki`, `jenis_kelamin`, `alamat`, `notelp`) VALUES ('" +idmuz+
                    "','"+namamuz +
                    "','"+jk+
                    "','"+address+
                    "','"+numb+"')";
            
            PreparedStatement ps = (PreparedStatement)con.prepareStatement(query);
            ps.execute();
            JOptionPane.showMessageDialog(null, "Berhasil Menyimpan");
            clearForm();
            showData();
            
        } catch (SQLException ex) {
            Logger.getLogger(datamuzakki.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
            }
        
        } 
        
        catch (SQLException ex) {
            Logger.getLogger(datamuzakki.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
    }//GEN-LAST:event_simpanActionPerformed

