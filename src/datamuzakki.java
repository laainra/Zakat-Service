    private void tabmuzakkiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabmuzakkiMouseClicked
        // TODO add your handling code here:
        int row =  tabmuzakki.rowAtPoint(evt.getPoint());
        String id_muzakki = tabmuzakki.getValueAt(row, 0).toString();
        idmuzakki.setText(id_muzakki);
        
        String nama = tabmuzakki.getValueAt(row, 1).toString();
        NamaMuzakki.setText(nama);
        
        if ("Perempuan".equals(tabmuzakki.getValueAt(row, 2).toString())){
                perempuan.setSelected(true);
        }else {
                lakilaki.setSelected(true);
        }
        
        String al = tabmuzakki.getValueAt(row, 3).toString();
        alamat.setText(al);
        
        String no = tabmuzakki.getValueAt(row, 4).toString();
        notelp.setText(no);
    }//GEN-LAST:event_tabmuzakkiMouseClicked

